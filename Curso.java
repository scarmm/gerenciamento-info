public class Curso {
    int Idcurso;
    String nomeCurso;
    int cargaHoraria;
    int IdProfessor;

    public Curso(int Idcurso, String nomeCurso, int cargaHoraria, int IdProfessor) {
        this.Idcurso = Idcurso;
        this.nomeCurso = nomeCurso;
        this.cargaHoraria = cargaHoraria;
        this.IdProfessor = IdProfessor;
    }
}
