public class Aluno {
    int IdAluno;
    String nomeAluno;
    String dtNasc;
    String CPF;
    int Idcurso;

    public Aluno(int IdAluno, String nomeAluno, String dtNasc, String CPF, int Idcurso) {
        this.IdAluno = IdAluno;
        this.nomeAluno = nomeAluno;
        this.dtNasc = dtNasc;
        this.CPF = CPF;
        this.Idcurso = Idcurso;
    }
}
